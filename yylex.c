static int yy_getc(void) {
    if (yy_buf_pos >= yy_buf_end) {
        if (yy_eof) return EOF;
        FILE* src = yyin ? yyin : stdin;
        int n = fread(yy_buffer, 1, YY_BUF_SIZE, src);
        if (n <= 0) { yy_eof = 1; return EOF; }
        yy_buf_end = n;
        yy_buf_pos = 0;
    }
    return (unsigned char)yy_buffer[yy_buf_pos++];
}

static void yy_ungetc(void) {
    if (yy_buf_pos > 0) yy_buf_pos--;
}

